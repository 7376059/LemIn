#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t u;

	if (!s1 || !s2)
		return (0);
	u = 0;
	while ((s1[u] || s2[u]) && u < n)
	{
		if (s1[u] != s2[u])
			return (0);
		u++;
	}
	return (1);
}
